package java_lessons;

public class Arrays {
    public static void main(String[] args) {
        String[] ogrenciler = new String[3];
        ogrenciler [0] = "Remzi";
        ogrenciler [1] = "Israfil";
        ogrenciler [2] = "Ahmet";
        for(String ogrenci:ogrenciler){
            System.out.println(ogrenci);
        }

        // arrays kullanımı bu yapılan işlemi daha basit hale getirir [] array işaretidir

    }
    }


