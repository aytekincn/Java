package java_lessons;

public class DoWhile {
    public static void main(String[] args) {
        int a =1;
        do{
            System.out.println(a);
            a++;
        }while(a<=10);
          // while döngüsünde şart uymazsa çalışmaz
        // do while döngüsünde şart uymasa bile while sonradan geldiği için önce do kısmını 1 kez çalıştırır ama devam etmez



    }
}
