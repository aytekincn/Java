package java_lessons;

public class WhileLoop2 {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 100) {
            System.out.println("Hello World:" + i++);
        }
        int z=1;
        while (z<55){
            System.out.println("Hello Universe: "+ z);
            z++;
        }
    }

}

